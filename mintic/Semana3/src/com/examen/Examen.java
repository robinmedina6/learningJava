package com.examen;

public class Examen {
	private String asignatura;
	private String aula;
	private Fecha fecha;
	private Hora hora;
	public Examen(String asignatura, String aula, Fecha fecha, Hora hora) {
		super();
		this.asignatura = asignatura;
		this.aula = aula;
		this.fecha = fecha;
		this.hora = hora;
	}
}
